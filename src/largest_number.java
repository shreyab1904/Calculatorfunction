public class largest_number {
    public static void main(String[] args) {
        int x=20,y=30,z=10;
        if(x>y && x>z)
        {
            System.out.println("x is highest");
        }
        else if(y>x && y>z)
        {
            System.out.println("Y is highest");
        }
        else
        {
            System.out.println("Z is highest");
        }
    }
}
